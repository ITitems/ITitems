/* ********************************************************
 * 특수문자 확인(특수문자 전부 제거)
 ******************************************************** */
function symbolCheck1(obj){
	let val = $(obj).val();
	let newVal = val.replace(/[\{\}\[\]\/?.,;:|\)*~`!^\-_+<>@\#$%&\\\=\(\'\"\s]/g, "");
	 $(obj).val(newVal);
}

